from typing import List


class Solution:
    def threeSum(self, nums: List[int]) -> List[List[int]]:
        # 给你一个整数数组 nums ，判断是否存在三元组 [nums[i], nums[j], nums[k]]
        # 满足 i != j、i != k 且 j != k ，同时还满足 nums[i] + nums[j] + nums[k] == 0 。
        # 请你返回所有和为 0 且不重复的三元组。
        #
        # 注意：答案中不可以包含重复的三元组。
        # [-1, 0, 1, 2, -1, -4]
        # [-4, -1, -1, 0, 1, 2]
        output = set()
        nums = sorted(nums)

        last_item = None
        for i, item in enumerate(nums):
            if last_item is None:
                last_item = item
            elif last_item == item:
                continue

            left = 0
            right = len(nums) - 1

            last_left = None
            last_right = None
            left_flag = False
            right_flag = False
            while left < right:
                if left == i or right == i:
                    left += 1
                    left_flag = True
                    continue

                if left_flag and nums[left] == last_left:
                    last_left = nums[left]
                    left += 1
                    left_flag = True

                if right_flag and nums[right] == last_right:
                    last_right = nums[right]
                    right -= 1
                    right_flag = True

                val = nums[left] + nums[right]
                current = val + item
                if current == 0:
                    output.add(tuple(sorted([nums[left], nums[right], item])))
                    left += 1
                    left_flag = True
                elif current < 0:
                    left += 1
                    left_flag = True
                else:
                    right -= 1
                    right_flag = True

        return [list(item) for item in output]


